package day03;
/**
 * 根据分数计算级别
 * */
public class Demo12 {

	public static void main(String[] args) {
		int score = 90;
		if(score>=90){
			System.out.println("A");			
		}else if(score>=80){
			System.out.println("B");
		}else if(score>=60){
			System.out.println("C");
		}else{
			System.out.println("D");
		}
	}

}

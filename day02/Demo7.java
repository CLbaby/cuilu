package day02;
/**
 * 浮点数
 * */
public class Demo7 {
	public static void main(String[] args){
		double pi = 3.1415926535897932384626;	//默认字面量double类型
		float f = 3.1415926535897932384626f;
		System.out.println(pi);	//精确度高
		System.out.println(f);	//精确度差
		
		double d = 2.6;
		System.out.println(d-2);	//浮点数做计算时不是特别精确
		
		
		
	}
}
